package com.example;
import java.util.Scanner;  

public class Ex {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        System.out.println(a);
        double b = sc.nextDouble();
        System.out.println(b);
        String c = sc.next();
        System.out.println(c);
        sc.close();
    }
}
